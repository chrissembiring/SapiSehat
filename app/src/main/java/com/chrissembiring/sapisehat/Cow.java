package com.chrissembiring.sapisehat;

// Cow setter-getter
public class Cow
{
    private String name;
    private int age;
    private int weight;

    public String getName()
    {
        return this.name;
    }

    public int getAge()
    {
        return this.age;
    }

    public int getWeight()
    {
        return this.weight;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public void setAge(int age)
    {
        this.age = age;
    }

    public void setWeight(int weight)
    {
        this.weight = weight;
    }
}